@dp.callback_query()
async def handle_inline_buttons(callback: types.CallbackQuery):
    """
    –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –≤—Å–µ—Ö inline-–∫–Ω–æ–ø–æ–∫.
    
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç:
    1. –ö–Ω–æ–ø–∫–∏ –≤—ã–±–æ—Ä–∞ –º–µ—Å—è—Ü–∞ (month_1, month_2, ...)
    2. –ö–Ω–æ–ø–∫—É –≤–æ–∑–≤—Ä–∞—Ç–∞ –≤ –º–µ–Ω—é (back_to_menu)
    """
    data = callback.data  # –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –∏–∑ –∫–Ω–æ–ø–∫–∏
    
    # === –û–ë–†–ê–ë–û–¢–ö–ê –í–´–ë–û–†–ê –ú–ï–°–Ø–¶–ê ===
    if data.startswith("month_"):
        try:
            # –ò–∑–≤–ª–µ–∫–∞–µ–º –Ω–æ–º–µ—Ä –º–µ—Å—è—Ü–∞ –∏–∑ callback_data
            month_num = int(data.split("_")[1])
            
            # –°–æ–±–∏—Ä–∞–µ–º –¥–∞–Ω–Ω—ã–µ –∑–∞ –≤—ã–±—Ä–∞–Ω–Ω—ã–π –º–µ—Å—è—Ü
            month_data = {}
            for day in range(1, 32):
                date_str = f"{day:02d}.{month_num:02d}"
                if date_str in prayer_data:
                    month_data[date_str] = prayer_data[date_str]
            
            # –ï—Å–ª–∏ –µ—Å—Ç—å –¥–∞–Ω–Ω—ã–µ –∑–∞ –º–µ—Å—è—Ü
            if month_data:
                response = format_month_table(month_data, month_num)
                await callback.message.edit_text(
                    response, 
                    parse_mode=None  # –ë–µ–∑ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è Markdown
                )
            else:
                month_name_ru = MONTHS_RU.get(month_num, f"–ú–µ—Å—è—Ü {month_num}")
                await callback.message.edit_text(
                    f"‚ùå –î–∞–Ω–Ω—ã–µ –Ω–∞ {month_name_ru} –Ω–µ –Ω–∞–π–¥–µ–Ω—ã",
                    parse_mode="Markdown"
                )
                
        except Exception as e:
            # –í —Å–ª—É—á–∞–µ –æ—à–∏–±–∫–∏ –ø–æ–∫–∞–∑—ã–≤–∞–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ
            await callback.message.edit_text(
                f"‚ùå –û—à–∏–±–∫–∞: {str(e)}",
                parse_mode="Markdown"
            )
    
    # === –û–ë–†–ê–ë–û–¢–ö–ê –ö–ù–û–ü–ö–ò –í–û–ó–í–†–ê–¢–ê ===
    elif data == "back_to_menu":
        # –£–¥–∞–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ —Å –≤—ã–±–æ—Ä–æ–º –º–µ—Å—è—Ü–∞
        await callback.message.delete()
        
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –≥–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é
        await callback.message.answer(
            "üëá *–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –º–µ–Ω—é –≤–Ω–∏–∑—É:*",
            parse_mode="Markdown",
            reply_markup=get_main_menu_keyboard()
        )
    
    # –ü–æ–¥—Ç–≤–µ—Ä–∂–¥–∞–µ–º –æ–±—Ä–∞–±–æ—Ç–∫—É callback (—É–±–∏—Ä–∞–µ–º —á–∞—Å–∏–∫–∏ –Ω–∞ –∫–Ω–æ–ø–∫–µ)
    await callback.answer()


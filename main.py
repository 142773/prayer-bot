        subscribed_users = set()  # –°–æ–∑–¥–∞–µ–º –ø—É—Å—Ç–æ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ –ø—Ä–∏ –æ—à–∏–±–∫–µ


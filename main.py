def schedule_prayer_notifications():
    """
    –ü–ª–∞–Ω–∏—Ä—É–µ—Ç —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –æ –Ω–∞–º–∞–∑–∞—Ö –Ω–∞ —Å–µ–≥–æ–¥–Ω—è.
    
    –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è:
    1. –£–¥–∞–ª—è–µ—Ç —Å—Ç–∞—Ä—ã–µ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è
    2. –ü–æ–ª—É—á–∞–µ—Ç —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ –Ω–∞ —Å–µ–≥–æ–¥–Ω—è
    3. –°–æ–∑–¥–∞–µ—Ç –∑–∞–¥–∞–Ω–∏—è –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –Ω–∞–º–∞–∑–∞
    
    –í—ã–∑—ã–≤–∞–µ—Ç—Å—è –ø—Ä–∏ —Å—Ç–∞—Ä—Ç–µ –±–æ—Ç–∞ –∏ –∫–∞–∂–¥—ã–π –¥–µ–Ω—å –≤ 00:01.
    """
    # –£–¥–∞–ª—è–µ–º –≤—Å–µ —Å—Ç–∞—Ä—ã–µ –∑–∞–¥–∞–Ω–∏—è
    scheduler.remove_all_jobs()
    
    # –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –Ω–∞ —Å–µ–≥–æ–¥–Ω—è
    today = datetime.now(TIMEZONE)
    today_str = today.strftime("%d.%m")
    times = prayer_data.get(today_str, {})
    
    # –ï—Å–ª–∏ –Ω–µ—Ç –¥–∞–Ω–Ω—ã—Ö –Ω–∞ —Å–µ–≥–æ–¥–Ω—è, –≤—ã—Ö–æ–¥–∏–º
    if not times:
        return
    
    # –°–ø–∏—Å–æ–∫ –Ω–∞–º–∞–∑–æ–≤ –¥–ª—è —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–π
    prayers = [
        ("–§–∞–¥–∂—Ä", times['Fajr']),
        ("–ó—É—Ö—Ä", times['Duhr']),
        ("–ê—Å—Ä", times['Asr']),
        ("–ú–∞–≥—Ä–∏–±", times['Maghrib']),
        ("–ò—à–∞", times['Isha'])
    ]
    
    # –°–æ–∑–¥–∞–µ–º –∑–∞–¥–∞–Ω–∏–µ –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –Ω–∞–º–∞–∑–∞
    for prayer_name, prayer_time_str in prayers:
        try:
            # –ü–∞—Ä—Å–∏–º –≤—Ä–µ–º—è –∏–∑ —Å—Ç—Ä–æ–∫–∏ "06:30"
            prayer_hour, prayer_minute = map(int, prayer_time_str.split(':'))
            
            # –°–æ–∑–¥–∞–µ–º datetime –æ–±—ä–µ–∫—Ç –¥–ª—è —Ç–æ—á–Ω–æ–≥–æ –≤—Ä–µ–º–µ–Ω–∏ –Ω–∞–º–∞–∑–∞
            prayer_datetime = today.replace(
                hour=prayer_hour, 
                minute=prayer_minute, 
                second=0, 
                microsecond=0
            )
            
            # –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–¥–∞–Ω–∏–µ –≤ –ø–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫
            scheduler.add_job(
                send_prayer_notification,  # –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–∑–æ–≤–∞
                CronTrigger(  # –¢—Ä–∏–≥–≥–µ—Ä –ø–æ –≤—Ä–µ–º–µ–Ω–∏
                    hour=prayer_datetime.hour,
                    minute=prayer_datetime.minute,
                    timezone=TIMEZONE
                ),
                args=[prayer_name, prayer_time_str, times],  # –ê—Ä–≥—É–º–µ–Ω—Ç—ã —Ñ—É–Ω–∫—Ü–∏–∏
                id=f"{prayer_name}_{today_str}"  # –£–Ω–∏–∫–∞–ª—å–Ω—ã–π ID –∑–∞–¥–∞–Ω–∏—è
            )
            
            print(f"‚è∞ –ó–∞–ø–ª–∞–Ω–∏—Ä–æ–≤–∞–Ω–æ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –¥–ª—è {prayer_name} –Ω–∞ {prayer_time_str}")
            
        except Exception as e:
            print(f"‚ùå –û—à–∏–±–∫–∞ –ø–ª–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏—è {prayer_name}: {e}")

